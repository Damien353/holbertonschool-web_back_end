#!/usr/bin/env python3
"""
Module 0-add
This module defines a function that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return the result.

    Args:
        a (float): First number
        b (float): Second number

    Returns:
        float: The sum of a and b
    """
    return a + b
