#!/usr/bin/env python3
"""
Module 3-to_str
This module defines a function that converts a float to a string.
"""


def to_str(n: float) -> str:
    """
    Convert a float to a string representation.

    Args:
        n (float): The number to convert

    Returns:
        str: The string representation of the float
    """
    return str(n)
